
print ("\nIf Else\n")

angka = float (input ("Masukan sembarang angka : "))
print (f"Angka yang dimasukan adalah {angka}\n")

hasil = (angka / 2) + 8

if hasil > angka:

    print (f"{hasil} > {angka}")
    print ("Mantap")

else :

    print (f"{hasil} < {angka}")
    print ("Jangan Menyerah")

print ("\nSampai jumpa")

print ("\n")