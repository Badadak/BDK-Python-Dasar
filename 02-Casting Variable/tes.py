
print ("\nCasting Variable\n")

a = 9
b = 8.5
c = "dalam"
d = False

print ("a = ", a, " type = ", type (a))
print ("b = ", b, " type = ", type (b))
print ("c = ", c, " type = ", type (c))
print ("d = ", d, " type = ", type (d))

print ("\n----- Casting -----\n")

a = float (a)
b = int (b)
c = bool (c)
d = int (d)

print ("a = ", a, " type = ", type (a))
print ("b = ", b, " type = ", type (b))
print ("c = ", c, " type = ", type (c))
print ("d = ", d, " type = ", type (d))

print ("\n")